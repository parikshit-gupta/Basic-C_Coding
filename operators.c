#include<stdio.h>

int main()
{
    int a,b;
    a=23;
    b=23;
    // arthmatic operators
    printf("a+b= %d\n", a+b);
    printf("a-b= %d\n", a-b);
    printf("a*b= %d\n", a*b);
    printf("a/b= %d\n", a/b);
    // note that a/b gives a integer as any arthmatic operator applied to two integers will always return a integer

    // relational operators
    printf("a==b:%d\n", a==b);
    return 0;
}
